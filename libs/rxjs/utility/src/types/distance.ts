/**
 * Supported distance types in the [[distance]] operator
 */
export enum Distances {
  /**
   * Centimeters
   */
  CM = 'cm',
  /**
   * Feet
   */
  FEET = 'feet',
  /**
   * Inches
   */
  INCHES = 'inches',
  /**
   * Kilometers
   */
  KM = 'km',
  /**
   * Meters
   */
  METERS = 'meters',
  /**
   * Miles
   */
  MILES = 'miles',
  /**
   * Yards
   */
  YARDS = 'yards',
}
